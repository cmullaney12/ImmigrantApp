DROP DATABASE IF EXISTS immigrant_project;
CREATE DATABASE immigrant_project;
USE immigrant_project;

CREATE TABLE State (
    sName VARCHAR(200) UNIQUE NOT NULL,
    PRIMARY KEY (sName)
);

CREATE TABLE City (
    cID INT NOT NULL AUTO_INCREMENT,
    cName VARCHAR(200) DEFAULT NULL,
    cState VARCHAR(200) DEFAULT NULL,
    PRIMARY KEY (cID),
    FOREIGN KEY (cState)
        REFERENCES State (sName)
        ON UPDATE CASCADE
);

CREATE TABLE ProcessLocation (
    plID INT NOT NULL AUTO_INCREMENT,
    plName VARCHAR(200) DEFAULT NULL,
    plCity INT DEFAULT NULL,
    PRIMARY KEY (plID),
    FOREIGN KEY (plCity)
        REFERENCES City (cID)
        ON UPDATE CASCADE
);
    
CREATE TABLE Continent (
    cName VARCHAR(200) UNIQUE NOT NULL,
    PRIMARY KEY (cName)
);

CREATE TABLE Country (
    cName VARCHAR(200) UNIQUE NOT NULL,
    cContinent VARCHAR(200) NOT NULL,
    PRIMARY KEY (cName),
    FOREIGN KEY (cContinent)
        REFERENCES Continent (cName)
        ON UPDATE CASCADE
);
    
CREATE TABLE Ethnicity (
    eName VARCHAR(200) UNIQUE NOT NULL,
    PRIMARY KEY (eName)
);

CREATE TABLE Languages (
    lName VARCHAR(45) UNIQUE NOT NULL,
    PRIMARY KEY (lName)
);
    
CREATE TABLE EthnicBackground (
    ebID INT NOT NULL AUTO_INCREMENT,
    country VARCHAR(200) DEFAULT NULL,
    ethnicity VARCHAR(200) DEFAULT NULL,
    spokenLang VARCHAR(200) DEFAULT NULL,
    PRIMARY KEY (ebID),
    CONSTRAINT FOREIGN KEY (country)
        REFERENCES Country (cName)
        ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FOREIGN KEY (ethnicity)
        REFERENCES Ethnicity (eName)
        ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FOREIGN KEY (spokenLang)
        REFERENCES Languages (lName)
        ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE Immigrant (
    immID INT NOT NULL AUTO_INCREMENT,
    immFirstName VARCHAR(45) DEFAULT NULL,
    immLastName VARCHAR(45) DEFAULT NULL,
    immGender VARCHAR(45),
    immDate DATE DEFAULT NULL,
    immEB INT DEFAULT NULL,
    immProcLoc INT DEFAULT NULL,
    immDestCity INT DEFAULT NULL,
    PRIMARY KEY (immID),
    CONSTRAINT FOREIGN KEY (immEB)
        REFERENCES EthnicBackground (ebID)
        ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FOREIGN KEY (immProcLoc)
        REFERENCES ProcessLocation (plID)
        ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FOREIGN KEY (immDestCity)
        REFERENCES City (cID)
        ON DELETE SET NULL ON UPDATE CASCADE
);

